figure;
hold on;
load(['Results\ARIMA_Denver_CO.mat']);
axis equal;
axis square;
plot(ys, m, '+', 'Color',rgb('green')); 
load(['Results\SVR_Denver_CO.mat']);
axis equal;
axis square;
plot(ys, m, 'o', 'Color',rgb('orange')); 
load(['Results\Denver_CO.mat']);
axis equal;
axis square;
plot(ys, m, '*', 'Color',rgb('red')); 
xlabel('Actual Wind Speed (mph)');
ylabel('Predicted Wind Speed (mph)');
legend('ARIMA','SVR','GMCM-GPR',2);
x_lim=get(gca,'xlim');
y_lim=get(gca,'ylim');
xlim([min([x_lim,y_lim]) max([x_lim,y_lim])]);
ylim([min([x_lim,y_lim]) max([x_lim,y_lim])]);
plot([0,100],[0,100],'--', 'Color',rgb('blue'), 'LineWidth',2);

saveas(gcf,['Figs\Final\Denver_CO_pred'],'fig');
maximize;
applytofig(gcf, 'width',8, 'height',8, 'color','rgb', 'resolution',1800, 'bounds','tight');
set(gcf, 'Renderer', 'painters');
saveas(gcf,['Figs\Final\Denver_CO_pred'],'png');
saveas(gcf,['Figs\Final\Denver_CO_pred'],'epsc');
clear all;
close all;
clc;

